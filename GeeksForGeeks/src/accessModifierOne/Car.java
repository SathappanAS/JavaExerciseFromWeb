package accessModifierOne;

public class Car {

	public void Wheels() {
		System.out.println("Car has four wheels");
	}
	
	protected void seat() {
		System.out.println("Car has one seat");
	}
	
	void window() {
		System.out.println("car--window");
	}
	
	private void name() {
		System.out.println("car--name");
	}
	
}
